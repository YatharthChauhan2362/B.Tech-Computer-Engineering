// Write a program to show output like:
// * * * * *
// * * * * 
// * * *
// * *
// *
public class Practical_1_6 {
		public static void main(String[] args)
		{
			int i,j;
			for(i=5;i>0;i--)
			{
				for(j=0;j<i;j++)
				{
					System.out.print(" * ");
				}
				System.out.println(" ");
			}
			System.out.println("\n20DCE019-Yatharth Chauhan");
		}
}